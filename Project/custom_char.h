/**
 * @brief Custom character struct.
 *
 * This struct represents a custom character with a specified width, height, and data array.
 *
 * @tparam N The size of the data array.
 */
template <size_t N>
struct custom_char {

    uint8_t char_width;
    uint8_t char_height;
    uint8_t data[N];

    custom_char(uint8_t width, uint8_t height, uint8_t (&data_array)[N])
        : char_width(width), char_height(height) {
        std::memcpy(data, data_array, N * sizeof(uint8_t));
    }
};

